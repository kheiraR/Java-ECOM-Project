package miage.ecom.web.view.vo;

import miage.ecom.entity.Product;


/**
 *
 * @author Schwartz Michaël <m.schwartz@epokmedia.fr>
 */
public class ProductVo extends ValueObject<Product> {
	
	public String image;
	public String name;
	public Long price;
	public Integer idCategory;
	public String reference;
	public Integer idProduct;
	public String description;
	
	public ProductVo() {
	
	}
	
	public ProductVo(Product product) throws Exception {
		
		this.extract(product);
		
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Integer getIdCategory() {
		return idCategory;
	}

	public void setIdCategory(Integer idCategory) {
		this.idCategory = idCategory;
	}

	public Integer getIdProduct() {
		return idProduct;
	}

	public void setIdProduct(Integer idProduct) {
		this.idProduct = idProduct;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getPrice() {
		return price;
	}

	public void setPrice(Long price) {
		this.price = price;
	}

	public String getReference() {
		return reference;
	}

	public void setReference(String reference) {
		this.reference = reference;
	}
	
	

}
